                <div>
                    <h4>Jordana Cepelewicz</h4>
                    <p>tags / biology cell biology developmental</p>
                </div>
                <div>
                    <p>The vast majority of telehealth caregivers in Virginia are confident in the quality of virtual
                        care.
                        Two experts from the Virginia Telehealth Network go in-depth about new poll data that shows its
                        staying power.

                        Eighty percent of providers using telehealth in Virginia have seen improvements in their
                        patients’
                        continuity of care, and 87% feel confident in the quality of care they’re able to offer patients
                        via
                        telehealth, according to a new survey conducted by the Virginia Telehealth Network of more than
                        10,500 active license healthcare professionals throughout the state.

                        These are just some of the impressive results from the new survey. Healthcare IT News sat down
                        with
                        Virginia Telehealth Network's Mara Servaites, executive director, and Robin Cummings, special
                        projects director, to dig into the results and find out the status of telemedicine in the state.
                    </p>
                </div>
                <div>
                    <h3>
                        Q. What have you learned about providers' plans to increase or decrease telehealth usage now
                        that
                        the public health emergency has ended? And why do you think this is?
                    </h3>
                </div>
                <div>
                    <p>Cummings: We saw some reduction in reliance on telehealth now that operations are returning to
                        pre-COVID normal, and I think it’s really interesting that we are seeing growth in providers
                        saying
                        telehealth offers them flexibility in their scheduling, and that it's allowing them to see more
                        patients.

                        It’s again getting to that normalization place of what makes sense for a provider and his or her
                        patients. A virtual practice is not going to necessarily work for all providers and that's okay.

                        Servaites: We’re beginning to get to a place that maximizes telehealth usage, while also
                        acknowledging that sometimes this isn't the right modality for this particular service or for
                        this
                        patient.

                        One of the other things that could impact long-term use of telehealth is around reimbursement,
                        specifically with the PHE expiring, that is creating a lot of chaos and a lot of misinformation.
                        We
                        are hearing concern from the healthcare community that telehealth may no longer be a
                        reimbursable
                        service and that even providers who have been doing telehealth for a long time may no longer be
                        reimbursed – while not true, the feeling of lack of permanence creates that anxiety around
                        long-range planning for providers.</p>
                </div>
                <div>
                    <h3>
                        Q. Your survey has discovered fascinating things about Virginia providers' feelings about
                        continuity
                        of care and quality of care they’re able to offer patients via telehealth. Why do you think this
                        is?
                    </h3>
                </div>
                <div>
                    <p>
                        Cummings: We tracked both of those points from the first survey into the second. We saw growth
                        in
                        the areas of providers agreeing or strongly agreeing that telehealth is an effective tool for
                        providing care. They feel confident in the quality of care they offer via telehealth and have
                        seen
                        improvements in their patients’ continuity of care.

                        Servaites: Part of that is a “practice makes perfect” scenario, and as people have gotten used
                        to
                        telehealth, they've also embedded better workflows within their practices, and so have their
                        collaborating providers. We’re seeing a “rising of all ships” as people get more used to the
                        modality and as documentation and workflows improve.

                        When providers are looking at serving patients beyond their local community, it’s important to
                        be
                        aware of what laws and regulations still exist for serving patients across state lines. They
                        need to
                        get clarity around what they're allowed to do legally, and certainly what their malpractice
                        coverage
                        will allow them to do.

                        They also need to regularly find out the location of the patient. Where is the patient calling
                        in
                        from? What's the address? If there was an emergency, how would you be able to, you know, call
                        for
                        help for that individual? But then it also is going to let you know, “has my patient traveled
                        across
                        state lines and am I now serving my patient over a state line?”
                    </p>
